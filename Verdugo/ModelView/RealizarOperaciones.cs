using System;
using System.Collections.Generic;
using System.Text;

namespace Verdugo.ModelView
{
    class RealizarOperaciones
    {
        public double suma(double Numero1, double Numero2, double Numero3)
        {
            return Numero1 + Numero2 + Numero3;
        }

        public double resta(double Numero1, double Numero2, double Numero3)
        {
            return Numero1 - Numero2 - Numero3;
        }

        public double Multiplicacion(double Numero1, double Numero2, double Numero3)
        {
            return Numero1 * Numero2 * Numero3;
        }

        public double Division(double Numero1, double Numero2, double Numero3)
        {
            return Numero1 / Numero2 / Numero3;
        }

    }
}
